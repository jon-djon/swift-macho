    @Test
    func splitSegInfoV2ParsesSuccessfully() throws {
        try Self.v2Data.withParserSpan { span in
            let info = try SplitSegInfo(parsing: &span)
            #expect(info.version == 2)
            #expect(info.fixups.count > 0)
        }
    }

    @Test
    func splitSegInfoV2SectionPairs() throws {
        try Self.v2Data.withParserSpan { span in
            let info = try SplitSegInfo(parsing: &span)

            // All fixups should have valid kinds
            for fixup in info.fixups {
                #expect(fixup.kind.rawValue >= 1 && fixup.kind.rawValue <= 12)
            }

            // Check we have fixups from multiple section pairs
            let fromIndices = Set(info.fixups.map { $0.fromSectionIndex })
            let toIndices = Set(info.fixups.map { $0.toSectionIndex })
            #expect(fromIndices.count >= 1)
            #expect(toIndices.count >= 1)
        }
    }

    @Test
    func splitSegInfoV2FirstFixup() throws {
        try Self.v2Data.withParserSpan { span in
            let info = try SplitSegInfo(parsing: &span)
            let first = info.fixups[0]

            // First section pair in the data: after 0x7F marker and count=36,
            // fromSectionIndex=1, toSectionIndex=0
            #expect(first.fromSectionIndex == 1)
            #expect(first.toSectionIndex == 0)
            #expect(first.fromSectionOffset > 0)
        }
    }

    @Test
    func splitSegInfoV2Displayable() throws {
        try Self.v2Data.withParserSpan { span in
            let info = try SplitSegInfo(parsing: &span)
