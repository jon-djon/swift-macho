@testable import SwiftMachO

struct LC_SEGMENT_SPLIT_INFO_Tests {
    // LC_SEGMENT_SPLIT_INFO header
    // cmd=0x1E, cmdsize=0x10 (16)
    // offset=0x4000, size=0x100
    let data = Data([
        0x1E, 0x00, 0x00, 0x00,  // cmd = LC_SEGMENT_SPLIT_INFO (0x1E)
        0x10, 0x00, 0x00, 0x00,  // cmdsize = 16
        0x00, 0x40, 0x00, 0x00,  // offset = 0x4000
        0x00, 0x01, 0x00, 0x00,  // size = 0x100
    ])

    // Wrong command ID
    let bad = Data([0x50, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00])

    @Test
    func loadCommandParsing() throws {
        try data.withParserSpan { span in
            let f = try SwiftMachO.LC_SEGMENT_SPLIT_INFO(parsing: &span, endianness: .little)
            #expect(f.header.id == .LC_SEGMENT_SPLIT_INFO)
            #expect(f.header.cmdSize == 16)
            #expect(f.range == 0..<16)
            #expect(f.offset == 0x4000)
            #expect(f.size == 0x100)
            #expect(span.count == 0)
        }
    }

    @Test
    func loadCommandInvalidID() throws {
        _ = bad.withParserSpan { span in
            #expect(throws: ParsingError.self) {
                _ = try SwiftMachO.LC_SEGMENT_SPLIT_INFO(parsing: &span, endianness: .little)
            }
        }
    }

    // MARK: - SplitSegInfo V1 Tests

    // V1 format: ULEB128 deltas terminated by 0
    // Deltas: 0x10, 0x20, 0x30, 0x00 (terminator)
    // Cumulative offsets: 0x10, 0x30, 0x60
    @Test
    func splitSegInfoV1() throws {
        let v1Data = Data([
            0x10,  // delta = 0x10
            0x20,  // delta = 0x20
            0x30,  // delta = 0x30
            0x00,  // terminator
        ])
        try v1Data.withParserSpan { span in
            let info = try SplitSegInfo(parsing: &span)
            #expect(info.version == 1)
            #expect(info.fixups.count == 3)
            #expect(info.fixups[0].fromSectionOffset == 0x10)
            #expect(info.fixups[1].fromSectionOffset == 0x30)
            #expect(info.fixups[2].fromSectionOffset == 0x60)
            // V1 defaults
            #expect(info.fixups[0].kind == .pointer64)
            #expect(info.fixups[0].fromSectionIndex == 0)
            #expect(info.fixups[0].toSectionIndex == 0)
        }
    }

